    private void timActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timActionPerformed
        String makh = JOptionPane.showInputDialog("Nhập mã Khách Hàng cần tim:");
        Vector<KhachHangDTO> arr = null;
        try {
            if (makh.trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Mã Khách Hàng cần tìm không tồn tại");
            } else {
                if (khBLL.Tim(makh)) {
                    arr = khBLL.timKhachHang(makh);
                    model = new DefaultTableModel();
                    model.addColumn("Mã Khách Hàng");
                    model.addColumn("Họ Tên");
                    model.addColumn("Ngày Sinh");
                    model.addColumn("Số Điện Thoại");
                    jTable.setModel(model);
                    for (int i = 0; i < arr.size(); i++) {
                        KhachHangDTO kh = arr.get(i);
                        makh = kh.getMaKH();
                        String hoten = kh.getTenKH();
                        String diachi = kh.getDiaChi();
                        String sdt = kh.getSdt();
                        txtmakh.setText(makh);
                        txttenkh.setText(hoten);
                        txtdiachi.setText(diachi);
                        txtsdt.setText(kh.getSdt());
                        Object[] row = {makh, hoten, diachi, sdt};
                        model.addRow(row);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Không tồn tại Mã Khách Hàng này");
                    txtmakh.setText("");
                    txttenkh.setText("");
                    txtsdt.setText("");
                    txtdiachi.setText("");
                    loadKhachHangList();
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Mã Khách Hàng không hợp lệ");
        }
    }//GEN-LAST:event_timActionPerformed

    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed
        try {
            if (txtmakh.getText().trim().equals("") || txttenkh.getText().trim().equals("")|| txtdiachi.getText().trim().equals("") || txtsdt.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn Khách Hàng");
            } else {
                KhachHangDTO kh = new KhachHangDTO();
                kh.setMaKH(txtmakh.getText());
                txtmakh.setEditable(false);
                kh.setTenKH(txttenkh.getText());
                kh.setDiaChi(txtdiachi.getText());
                kh.setSdt(txtsdt.getText());
                JOptionPane.showMessageDialog(this, khBLL.suaKhachHang(kh));
                loadKhachHangList();

            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }
    }//GEN-LAST:event_suaActionPerformed

