    private void exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportActionPerformed
        try {
            //tạo một đối tượng của lớp HSSFWorkbook
            HSSFWorkbook workbook = new HSSFWorkbook();
            //gọi phương thức creatSheet() và truyền tên file muốn tạo
            HSSFSheet sheet = workbook.createSheet("Nhân viên");
            //tạo hàng thứ 0 sử dụng phương thức createRow()
            HSSFRow rowhead = sheet.createRow((short) 0);
            //tạo ô bằng cách sử dụng phương thức createCell()
            //và thiết lập giá trị cho ô bằng cách sử dụng phương thức setCellValue()

            rowhead.createCell(0).setCellValue("Mã Khách Hàng");
            rowhead.createCell(1).setCellValue("Họ Tên");
            rowhead.createCell(2).setCellValue("Ngày Sinh");
            rowhead.createCell(3).setCellValue("Số Điện Thoại");
            Vector<KhachHangDTO> arr = new Vector<KhachHangDTO>();
            arr = khBLL.getAllKhachHang();
            for (int i = 0; i < arr.size(); i++) {
                KhachHangDTO kh = arr.get(i);
                String makh = kh.getMaKH();
                String hoten = kh.getTenKH();
                String diachi = kh.getDiaChi();
                String sdt = kh.getSdt();
                HSSFRow row = sheet.createRow((short) i+1);
                row.createCell(0).setCellValue(makh);
                row.createCell(1).setCellValue(hoten);
                row.createCell(2).setCellValue(diachi);
                row.createCell(3).setCellValue(sdt);
            }
            JFileChooser chooser = new JFileChooser();
            String filename = "";
            int i = chooser.showSaveDialog(chooser);
            if (i == JFileChooser.APPROVE_OPTION) {
                File file = chooser.getSelectedFile();
                try{

                    filename = file + ".xls";
                } catch (Exception e2) {
                    JOptionPane.showMessageDialog(null, "Lỗi khi lưu file!"); }
            }

            FileOutputStream fileOut = new FileOutputStream(filename);
            workbook.write(fileOut);
            //đóng stream
            fileOut.close();
            //đóng workbook
            workbook.close();
            //in thông báo tạo thành công
            JOptionPane.showMessageDialog(this, "File Excel đã được tạo thành công.");
        } catch (Exception e) {
            e.printStackTrace();}
    }//GEN-LAST:event_exportActionPerformed

