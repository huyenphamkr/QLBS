    private void xuatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xuatActionPerformed
        try {
        //tạo một đối tượng của lớp HSSFWorkbook
        HSSFWorkbook workbook = new HSSFWorkbook();
        //gọi phương thức creatSheet() và truyền tên file muốn tạo
        HSSFSheet sheet = workbook.createSheet("Nhân viên");
        //tạo hàng thứ 0 sử dụng phương thức createRow()
        HSSFRow rowhead = sheet.createRow((short) 0);
        //tạo ô bằng cách sử dụng phương thức createCell()
        //và thiết lập giá trị cho ô bằng cách sử dụng phương thức setCellValue()
        rowhead.createCell(0).setCellValue("Mã Sach");
        rowhead.createCell(1).setCellValue("Tên Sách");
        rowhead.createCell(2).setCellValue("Tác Giả");
        rowhead.createCell(3).setCellValue("Thể Loại");
        rowhead.createCell(4).setCellValue("Giá");
        rowhead.createCell(5).setCellValue("Số Lượng");
        Vector<SachDTO> arr = new Vector<SachDTO>();
        arr = sBLL.getAllSach();
        for (int i = 0; i < arr.size(); i++) {
            SachDTO nv = arr.get(i);
            String mas = nv.getMaSach();
            String ten = nv.getTenSach();
            String tg = nv.getTacGia();
            String tl = nv.getTheLoai();
            Double gia = nv.getGia();
            int sl = nv.getSoLuong();
            HSSFRow row = sheet.createRow((short) i+1);
            row.createCell(0).setCellValue(mas);
            row.createCell(1).setCellValue(ten);
            row.createCell(2).setCellValue(tg);
            row.createCell(3).setCellValue(tl); 
            row.createCell(4).setCellValue(gia);
            row.createCell(5).setCellValue(sl);             
        }         
        JFileChooser chooser = new JFileChooser();
        String filename = "";        
        int i = chooser.showSaveDialog(chooser);
        if (i == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
        try{
     
             filename = file + ".xls";
        } catch (Exception e2) {
                JOptionPane.showMessageDialog(null, "Lỗi khi lưu file!"); }
        }
   
        FileOutputStream fileOut = new FileOutputStream(filename);
        workbook.write(fileOut);
        //đóng stream
        fileOut.close();
        //đóng workbook
        workbook.close();
        //in thông báo tạo thành công
        JOptionPane.showMessageDialog(this, "File Excel đã được tạo thành công.");
    } catch (Exception e) {
      e.printStackTrace();}
    }//GEN-LAST:event_xuatActionPerformed

