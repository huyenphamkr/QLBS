                SachDTO s = new SachDTO();
                s.setMaSach(txtmas.getText());
                s.setTenSach(txttens.getText());
                s.setTacGia(txttacgia.getText());
                s.setTheLoai(txttheloai.getText());
                s.setGia(Double.parseDouble(txtgia.getText()));
                s.setHinhAnh(txtanh.getText());
