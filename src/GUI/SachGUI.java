    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        try {
            if (txtmas.getText().trim().equals("")
                || txttens.getText().trim().equals("")
                || txttacgia.getText().trim().equals("")
                || txttheloai.getText().trim().equals("")
                || txtgia.getText().trim().equals("")
                || txtanh.getText().trim().equals("")
                || txtsoluong.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin");
            }
            else {
                if (Integer.valueOf(txtmas.getText()) < 0) {
                    JOptionPane.showMessageDialog(null, "Mã sách phải là số nguyên lớn hơn 0");
                    return;
                }
                if (Integer.valueOf(txtsoluong.getText()) < 0) {
                    JOptionPane.showMessageDialog(null, "Số lượng sách phải là số nguyên lớn hơn 0");
                    return;
                }
                if (Double.valueOf(txtgia.getText()) < 0) {
                    JOptionPane.showMessageDialog(null, "Giá sách phải là số lớn hơn 0");
                    return;
                }
                SachDTO s = new SachDTO();
                s.setMaSach(txtmas.getText());
                s.setTenSach(txttens.getText());
                s.setTacGia(txttacgia.getText());
                s.setTheLoai(txttheloai.getText());
                s.setGia(Double.parseDouble(txtgia.getText()));
                s.setHinhAnh(txtanh.getText());
                s.setSoLuong(Integer.parseInt(txtsoluong.getText()));
                JOptionPane.showMessageDialog(this, sBLL.themSach(s));
                loadSachList();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }
    }//GEN-LAST:event_themActionPerformed
public void selectFile() {
        JFileChooser chooser = new JFileChooser();
        // optionally set chooser options ...
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File f = chooser.getSelectedFile();
            // Display image in GUI
            String url = f.getPath();
            Image img = otherFunction.resizeImage(url, l);
            ImageIcon imgIcon = new ImageIcon(img);
            l.setIcon(imgIcon);
            txtanh.setText(otherFunction.getNameImage(url));
            otherFunction.copyImage(url);
        } else {
            //System.out.println()
        }
    }
